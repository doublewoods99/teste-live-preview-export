import express from 'express';
import puppeteer from 'puppeteer';
import cors from 'cors';

const app = express();
const PORT = 3001;

// Middleware
app.use(cors());
app.use(express.json({ limit: '10mb' })); // Allow large HTML content

// Global browser instance
let browser = null;

// Initialize Puppeteer browser
async function initBrowser() {
  try {
    console.log('ðŸŽ­ Initializing Puppeteer browser...');
    browser = await puppeteer.launch({
      headless: true,
      args: [
        '--no-sandbox',
        '--disable-setuid-sandbox',
        '--disable-dev-shm-usage',
        '--disable-accelerated-2d-canvas',
        '--no-first-run',
        '--no-zygote',
        '--disable-gpu'
      ]
    });
    console.log('âœ… Puppeteer browser initialized');
  } catch (error) {
    console.error('âŒ Failed to initialize browser:', error);
    process.exit(1);
  }
}

// Health check endpoint
app.get('/health', (req, res) => {
  const isConnected = browser && browser.isConnected();
  res.json({
    status: 'OK',
    message: 'Resume PDF Generator Server',
    timestamp: new Date().toISOString(),
    browserConnected: isConnected
  });
});

// Main PDF generation endpoint
app.post('/generate-pdf', async (req, res) => {
  console.log('ðŸŽ¯ Received PDF generation request');
  
  try {
    const { htmlContent, filename = 'resume.pdf' } = req.body;
    
    if (!htmlContent) {
      return res.status(400).json({ 
        error: 'HTML content is required',
        message: 'Please provide htmlContent in the request body'
      });
    }

    if (!browser || !browser.isConnected()) {
      console.log('ðŸ”„ Browser disconnected, reinitializing...');
      await initBrowser();
    }

    // Create a new page
    const page = await browser.newPage();
    
    try {
      console.log('ðŸ“„ Setting HTML content in Puppeteer...');
      
      // Set the HTML content (this is the exact Preview output!)
      await page.setContent(htmlContent, {
        waitUntil: ['load', 'networkidle0'],
        timeout: 30000
      });

      console.log('ðŸŽ¨ Generating PDF...');
      
      // Generate PDF with A4 settings to match our layout
      const pdfBuffer = await page.pdf({
        format: 'A4',
        printBackground: true,
        margin: {
          top: '0px',
          right: '0px', 
          bottom: '0px',
          left: '0px'
        },
        preferCSSPageSize: true,
        displayHeaderFooter: false,
        scale: 1
      });

      console.log('âœ… PDF generated successfully');

      // Set response headers for PDF download
      res.setHeader('Content-Type', 'application/pdf');
      res.setHeader('Content-Disposition', `attachment; filename="${filename}"`);
      res.setHeader('Content-Length', pdfBuffer.length);
      
      // Send the PDF
      res.send(pdfBuffer);

    } catch (pageError) {
      console.error('âŒ Page processing error:', pageError);
      throw pageError;
    } finally {
      // Always close the page
      await page.close();
      console.log('ðŸ§¹ Page closed');
    }

  } catch (error) {
    console.error('âŒ PDF generation failed:', error);
    res.status(500).json({ 
      error: 'PDF generation failed', 
      message: error.message 
    });
  }
});

