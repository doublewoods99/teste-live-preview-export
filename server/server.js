// Graceful shutdown
process.on('SIGINT', async () => {
  console.log('\nðŸ›‘ Shutting down server...');
  if (browser) {
    await browser.close();
    console.log('ðŸ§¹ Browser closed');
  }
  process.exit(0);
});

// Start server
async function startServer() {
  await initBrowser();
  
  app.listen(PORT, () => {
    console.log(`ðŸš€ Resume PDF Generator Server running on http://localhost:${PORT}`);
    console.log(`ðŸ“Š Health check: http://localhost:${PORT}/health`);
    console.log(`ðŸ§ª Test endpoint: http://localhost:${PORT}/test-pdf`);
  });
}

startServer().catch(console.error); 